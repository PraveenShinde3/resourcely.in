const categories = links.reduce((acc, link) => {
  const allCategory = acc.find((item) => item.name === "All");
  if (allCategory) {
    allCategory.count += 1;
  } else {
    acc.push({ name: "All", count: 1, url: "/" });
  }

  const existingCategory = acc.find((item) => item.name === link.category);

  if (existingCategory) {
    existingCategory.count += 1;
  } else {
    acc.push({
      name: link.category,
      count: 1,
      url: "/categories/" + link.category.toLowerCase(),
    });
  }

  return acc;
}, []);
